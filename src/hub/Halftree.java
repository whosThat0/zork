package hub;

public class Halftree {


    public static void main(String[] args) {

            int rows = 4;

            for (int b = 1; b <= rows; ++b) {
                for (int t = 1; t <= b; ++t) {
                    System.out.print("* ");

                }
                System.out.println();

            }
            System.out.println("*");
    }
}
